<?php
/**
 * DebugConfig.php
 * デバッグ設定を管理するファイル
 * このファイルはオートローダーによって定義されているため、どこからでも呼び出すことができます。
 */

/*******************************************
 * セッションデバッグ情報の表示設定
 * true: セッション情報を表示
 * false: セッション情報を非表示
 *******************************************/
define('SHOW_SESSION_DEBUG', false);

/*******************************************
 * セッションデバッグ情報を表示するか判定
 *******************************************/
function isSessionDebugEnabled()
{
    return defined('SHOW_SESSION_DEBUG') && SHOW_SESSION_DEBUG === true;
}
